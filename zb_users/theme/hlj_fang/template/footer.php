function sl() {
    if (i - j > $('#scroll1 a:eq(0)').width()) {
        $('#scroll1 a:eq(0)').appendTo($('#scroll1'));
        j = i;
    }
    j--;
    $('#scroll1').offset({
        left: j
    });
}
